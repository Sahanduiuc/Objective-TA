package objectiveta.objects.input;

public class CandleStick {

	double open;
	double high;
	double low;
	double close;
	
	public CandleStick(){}
	
	public CandleStick(double open, double high, double low, double close) 
	{
		this.open = open;
		this.high = high;
		this.low = low;
		this.close = close;
	}
	
}
